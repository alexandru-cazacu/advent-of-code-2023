/**
 * Computes the sum of an unsolved matrix, without modifying it.
 */
function computeSumInPlace(matrix: string[][]): number {
  let sum = 0;
  let height = matrix.length;
  for (let x = 0; x < matrix[0].length; x++) {
    let firstObstacle = 0;
    for (let y = 0; y < matrix.length; y++) {
      if (matrix[y][x] == ".") {
        firstObstacle++;
      }
      if (matrix[y][x] == "O") {
        sum += height - y + firstObstacle;
      }
      if (matrix[y][x] == "#") {
        firstObstacle = 0;
      }
    }
  }
  return sum;
}

/**
 * Computes the sum of an already solved matrix.
 */
function computeSum(matrix: string[][]): number {
  let sum = 0;
  let height = matrix.length;
  for (let y = 0; y < matrix.length; y++) {
    for (let x = 0; x < matrix[y].length; x++) {
      if (matrix[y][x] === "O") {
        sum += height - y;
      }
    }
  }
  return sum;
}

